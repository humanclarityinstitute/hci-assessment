def typicality_sentence(dimension: str, percentile: Any) -> str:
    pos = percentile_position(percentile)
    try:
        p = int(round(float(percentile)))
    except Exception:
        p = 50

    if p >= 71:
        tone = "high"
        template = SECTION_3_TEMPLATES["distinctive_high"]
    elif p <= 40:
        tone = "low"
        template = SECTION_3_TEMPLATES["distinctive_low"]
    else:
        tone = "typical"
        template = SECTION_3_TEMPLATES["typical"]

    return template.format(
        position=pos,
        dimension=DIMENSION_LABELS.get(dimension, dimension).lower(),
        plain_meaning=DIMENSION_PLAIN_MEANINGS.get(dimension, {}).get(tone, ""),
    )


