def ordinal(n: Any) -> str:
    try:
        n = int(round(float(n)))
    except Exception:
        n = 0
    suffix = "th" if 10 <= n % 100 <= 20 else {1: "st", 2: "nd", 3: "rd"}.get(n % 10, "th")
    return f"{n}{suffix}"


def percentile_position(percentile: Any) -> str:
    try:
        p = int(round(float(percentile)))
    except Exception:
        p = 50

    if p >= 96:
        return "exceptionally high"
    if p >= 86:
        return "notably high"
    if p >= 71:
        return "above population centre"
    if p >= 41:
        return "near population centre"
    if p >= 26:
        return "below population centre"
    if p >= 11:
        return "notably low"
    return "exceptionally low"


def protect_position(percentile: Any) -> str:
    try:
        p = int(round(float(percentile)))
    except Exception:
        p = 50

    if p >= 71:
        return "at the high end"
    if p >= 41:
        return "in the middle"
    return "at the low end"


def plain_percentile_score(percentile: Any) -> str:
    try:
        p = int(round(float(percentile)))
    except Exception:
        p = 50
    return f"Higher than {p} of 100 people"


